# sets itu ga boleh kembar sets tidak ada index jadi ga biasa akses dengan index

angka1 ={1,2,3,4,5}
angka2 ={4,5,6,7,8}
# menggabungkan data angka 1&2 tanpa menulis dobel
print(angka1 | angka2 )
# menaampilkan angka1 yang tdk ada di angka2 dan sebaliknya
print(angka1 - angka2 )
print(angka2 - angka1 )
# menampilkan angka yang tdk ada kembarannya
print(angka1 ^ angka2)
# menampilkan angka yang sama 
print(angka1 & angka2)
